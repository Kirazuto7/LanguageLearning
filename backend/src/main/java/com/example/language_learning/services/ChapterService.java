package com.example.language_learning.services;

import com.example.language_learning.dto.lessons.*;
import com.example.language_learning.dto.models.ChapterMetadataDTO;
import com.example.language_learning.dto.models.PageDTO;
import com.example.language_learning.entity.models.Chapter;
import com.example.language_learning.entity.models.Page;
import com.example.language_learning.mapper.DtoMapper;
import com.example.language_learning.repositories.ChapterRepository;
import com.example.language_learning.requests.ChapterGenerationRequest;
import com.example.language_learning.dto.models.ChapterDTO;
import com.example.language_learning.entity.models.LessonBook;
import jakarta.transaction.Transactional;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.stereotype.Service;
import reactor.core.publisher.Mono;
import reactor.core.scheduler.Schedulers;
import reactor.util.function.Tuples;

import java.util.concurrent.atomic.AtomicInteger;
import java.util.concurrent.atomic.AtomicReference;

@Service
@Slf4j
@RequiredArgsConstructor
public class ChapterService {

    private final BookService bookService;
    private final AIService aiService;
    private final VocabularyLessonService vocabularyLessonService;
    private final GrammarLessonService grammarLessonService;
    private final ConjugationLessonService conjugationLessonService;
    private final PracticeLessonService practiceLessonService;
    private final ReadingComprehensionLessonService readingComprehensionLessonService;
    private final ProgressService progressService;
    private final ChapterRepository chapterRepository;
    private final PageService pageService;
    private final DtoMapper dtoMapper;

    /**
     * A public record to act as a clean data carrier for the various DTOs
     * generated by the AI service, avoiding long parameter lists.
     */
    private record GenerationContext(
            ChapterMetadataDTO metadata,
            VocabularyLessonDTO vocabulary,
            LessonDTO specificLesson
    ){}
    public record BookContext(LessonBook book, int nextChapterNumber) {}
    private final AtomicReference<Long> chapterIdForCleanup = new AtomicReference<>();

    public void generateNewChapterStream(ChapterGenerationRequest request, String taskId) {
        getBookContext(request)
                .publishOn(Schedulers.boundedElastic()) // Switch to a blocking-safe scheduler for the entire pipeline
                .flatMap(bookContext -> {
                    progressService.sendUpdate(taskId, 10, "Generating chapter outline...");
                    return aiService.generateChapterMetadata(request)
                            .map(metadata -> Tuples.of(bookContext, metadata));
                })
                .flatMap(tuple -> createInitialChapter(tuple.getT1(), tuple.getT2()))
                .doOnNext(chapter -> chapterIdForCleanup.set(chapter.getId())) // Store the chapter ID for potential cleanup
                .flatMap(chapter -> generateAndStreamPages(request, taskId, chapter))
                .subscribe(

                );
    }

    private Mono<ChapterService.BookContext> getBookContext(ChapterGenerationRequest request) {
        return Mono.fromCallable(() -> {
            LessonBook book = bookService.findOrCreateBook(request.language(), request.difficulty(), request.userId());
            int nextChapterNumber = book.getChapters().stream()
                    .mapToInt(Chapter::getChapterNumber)
                    .max()
                    .orElse(0) + 1;
            return new BookContext(book, nextChapterNumber);
        });
    }

    private Mono<Chapter> createInitialChapter(BookContext bookContext, ChapterMetadataDTO metadata) {
        return Mono.fromCallable(() -> {
            Chapter chapter = new Chapter();
            chapter.setLessonBook(bookContext.book());
            chapter.setChapterNumber(bookContext.nextChapterNumber());
            chapter.setTitle(metadata.title());
            chapter.setNativeTitle(metadata.nativeTitle());
            return chapterRepository.save(chapter);
        });
    }

    private Mono<Page> generateAndStreamPages(ChapterGenerationRequest request, String taskId, Chapter chapter) {
        ChapterMetadataDTO metadata = new ChapterMetadataDTO(chapter.getTitle(), chapter.getNativeTitle(), request.topic());
        AtomicInteger pageCounter = new AtomicInteger(1);

        return Mono.defer(() -> {
            progressService.sendUpdate(taskId, 25, "Creating vocabulary lesson...");
            return vocabularyLessonService.generateLesson(request, metadata);
        })
            .flatMap(lesson -> persistAndSendPage(taskId, chapter, lesson, pageCounter.getAndIncrement(), 40, "Vocabulary created."))
            .map(page -> new GenerationContext(metadata, (VocabularyLessonDTO) page.getLesson(), null))
            .flatMap(context -> generateLessonPath(request, taskId, chapter, context, pageCounter));
    }

    private Mono<Page> generateLessonPath(ChapterGenerationRequest request, String taskId, BookContext bookContext,
                                                         ChapterMetadataDTO metadata, VocabularyLessonDTO vocabulary) {
        if (bookContext.nextChapterNumber() % 2 != 0) {
            progressService.sendUpdate(taskId, 50, "Explaining grammar rules...");
            return aiService.generateGrammarLesson(request, vocabulary)
                    .doOnSuccess(g -> progressService.sendUpdate(taskId, 75, "Building practice exercises..."))
                    .flatMap(grammar -> aiService.generatePracticeLesson(request, vocabulary, grammar)
                            .doOnSuccess(p -> progressService.sendUpdate(taskId, 90, "Writing reading passage..."))
                            .flatMap(practice -> aiService.generateReadingComprehensionLesson(request, vocabulary, grammar)
                                    .map(reading -> new GeneratedComponents(metadata, vocabulary, grammar, null, practice, reading))
                            )
                    );
        } else {
            progressService.sendUpdate(taskId, 50, "Explaining conjugation rules...");
            return aiService.generateConjugationLesson(request, vocabulary)
                    .doOnSuccess(c -> progressService.sendUpdate(taskId, 75, "Building practice exercises..."))
                    .flatMap(conjugation -> aiService.generatePracticeLesson(request, vocabulary, conjugation)
                            .doOnSuccess(p -> progressService.sendUpdate(taskId, 90, "Writing reading passage..."))
                            .flatMap(practice -> aiService.generateReadingComprehensionLesson(request, vocabulary, conjugation)
                                    .map(reading -> new GeneratedComponents(metadata, vocabulary, null, conjugation, practice, reading))
                            )
                    );
        }
    }
}
