package com.example.language_learning.services;

import com.example.language_learning.dto.lessons.*;
import com.example.language_learning.dto.models.ChapterDTO;
import com.example.language_learning.dto.models.ChapterMetadataDTO;
import com.example.language_learning.entity.models.Chapter;
import com.example.language_learning.entity.user.User;
import com.example.language_learning.mapper.DtoMapper;
import com.example.language_learning.repositories.ChapterRepository;
import com.example.language_learning.requests.ChapterGenerationRequest;
import com.example.language_learning.entity.models.LessonBook;
import com.example.language_learning.responses.GenerationResponse;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;
import reactor.core.publisher.Mono;
import reactor.core.scheduler.Schedulers;

import java.util.UUID;
import java.util.concurrent.atomic.AtomicInteger;

@Service
@Slf4j
@RequiredArgsConstructor
public class ChapterService {

    private final LessonBookService lessonBookService;
    private final AIService aiService;
    private final VocabularyLessonService vocabularyLessonService;
    private final GrammarLessonService grammarLessonService;
    private final ConjugationLessonService conjugationLessonService;
    private final PracticeLessonService practiceLessonService;
    private final ReadingComprehensionLessonService readingComprehensionLessonService;
    private final ProgressService progressService;
    private final ChapterRepository chapterRepository;
    private final PageService pageService;
    private final DtoMapper dtoMapper;

    /**
     * A public record to act as a clean data carrier for the various DTOs
     * generated by the AI service, avoiding long parameter lists.
     */
    private record GenerationContext(
            VocabularyLessonDTO vocabulary,
            LessonDTO specificLesson
    ){}
    public record BookContext(LessonBook book, int nextChapterNumber, int nextPageNumber) {}

    public ChapterDTO getChapterById(Long chapterId, User user) {
        return chapterRepository.findByIdAndUserWithPages(chapterId, user)
                .map(dtoMapper::toDto)
                .orElse(null);
    }

    @Transactional
    public GenerationResponse prepareChapterGeneration(ChapterGenerationRequest request, Long userId) {
        String taskId = UUID.randomUUID().toString();

        // 1. Prepare the reactive pipelines for the setup tasks.
        Mono<BookContext> bookContextMono = getBookContext(request, userId).cache();
        Mono<ChapterMetadataDTO> metadataDTOMono = aiService.generateChapterMetadata(request);

        // 2. Synchronously create the chapter shell required content for initial response
        Chapter chapter = Mono.zip(bookContextMono, metadataDTOMono)
                .flatMap(tuple -> createInitialChapter(tuple.getT1(), tuple.getT2()))
                .block();

        // 3. Save the parent entity so that the cascade will handle persisting the new Chapter.
        LessonBook persistedBook = lessonBookService.save(bookContextMono.block().book());
        Chapter persistedChapter = persistedBook.getChapters().getLast();

        // 4. Return the chapter shell response to the client as the pages are building
        return GenerationResponse.builder()
                .taskId(taskId)
                .chapter(dtoMapper.toDto(persistedChapter))
                .build();
    }

    public void generateChapterAsync(ChapterGenerationRequest request, Long userId, String taskId, Long chapterId) {
        // Asynchronously start the page(s) generation for the chapter generation subscription
        Chapter chapter = chapterRepository.findById(chapterId).orElseThrow();
        getBookContext(request, userId).subscribe(bookContext ->
            generateNewChapterStream(request, taskId, chapter, bookContext)
        );
    }

    private void generateNewChapterStream(ChapterGenerationRequest request, String taskId, Chapter chapter, BookContext bookContext) {
        Mono.fromRunnable(() ->
                progressService.sendUpdate(taskId, 10, "Generating chapter outline...")
        )
        .subscribeOn(Schedulers.boundedElastic())
        .then(generateAndStreamPages(request, taskId, chapter, bookContext.nextPageNumber()))
        .doOnError(error -> {
            log.error("Chapter generation failed for task {}: {}", taskId, error.getMessage(), error);
            progressService.sendError(taskId, error);
        })
        .subscribe(
                null,
                error -> log.warn("Error propagated to subscribe for task {}. Should be handled by doOnError.", taskId, error),
                () -> log.info("Chapter generation stream for task {} completed.", taskId)
        );
    }

    private Mono<BookContext> getBookContext(ChapterGenerationRequest request, Long userId) {
        return Mono.fromCallable(() -> {
            LessonBook book = lessonBookService.findOrCreateBook(request.language(), request.difficulty(), userId);
            int nextChapterNumber = book.getChapters().stream()
                    .mapToInt(Chapter::getChapterNumber)
                    .max()
                    .orElse(0) + 1;

            int lastPageNumber = pageService.getLastPageNumberForBook(book.getId());

            return new BookContext(book, nextChapterNumber, lastPageNumber + 1);
        });
    }

    private Mono<Chapter> createInitialChapter(BookContext bookContext, ChapterMetadataDTO metadata) {
        return Mono.fromCallable(() -> {
            Chapter newChapter = Chapter.builder()
                            .lessonBook(bookContext.book())
                            .chapterNumber(bookContext.nextChapterNumber())
                            .title(metadata.title())
                            .nativeTitle(metadata.nativeTitle())
                            .build();
            bookContext.book().addChapter(newChapter);
            return newChapter;
        });
    }

    private Mono<Void> generateAndStreamPages(ChapterGenerationRequest request, String taskId, Chapter chapter, int startingPageNumber) {
        ChapterMetadataDTO metadata = new ChapterMetadataDTO(chapter.getTitle(), chapter.getNativeTitle(), request.topic());
        AtomicInteger pageCounter = new AtomicInteger(startingPageNumber);

        return generateVocabularyLessonPage(request, taskId, chapter, metadata, pageCounter)
                .flatMap(context -> generateGrammarOrConjugationLessonPage(request, taskId, chapter, pageCounter, context))
                .flatMap(context -> generatePracticeLessonPage(request, taskId, chapter, pageCounter, context))
                .flatMap(context -> generateReadingLessonPage(request, taskId, chapter, pageCounter, context))
                .then();
    }

    private Mono<GenerationContext> generateVocabularyLessonPage(ChapterGenerationRequest request, String taskId, Chapter chapter, ChapterMetadataDTO metadata, AtomicInteger pageCounter) {
        return Mono.fromRunnable(() -> progressService.sendUpdate(taskId, 25, "Creating vocabulary lesson..."))
                .subscribeOn(Schedulers.boundedElastic())
                .then(vocabularyLessonService.generateLesson(request, metadata))
                .flatMap(lesson -> pageService.createAndPersistPage(chapter, lesson, pageCounter.getAndIncrement()))
                .doOnNext(page -> progressService.sendPageUpdate(taskId, 40, "Vocabulary created.", chapter.getId(), dtoMapper.toDto(page)))
                .map(page -> {
                    VocabularyLessonDTO vocabDto = (VocabularyLessonDTO) dtoMapper.toDto(page.getLesson());
                    return new GenerationContext(vocabDto, null);
                });
    }

    private Mono<GenerationContext> generateGrammarOrConjugationLessonPage(ChapterGenerationRequest request, String taskId, Chapter chapter, AtomicInteger pageCounter, GenerationContext context) {
        // Generate Grammar Lesson for odd chapters and Conjugation Lesson for even chapters
        if (chapter.getChapterNumber() % 2 != 0) {
            return Mono.fromRunnable(() ->  progressService.sendUpdate(taskId, 50, "Explaining grammar rules..."))
                    .subscribeOn(Schedulers.boundedElastic())
                    .then(grammarLessonService.generateLesson(request, context.vocabulary()))
                    .flatMap(lesson -> pageService.createAndPersistPage(chapter, lesson, pageCounter.getAndIncrement()))
                    .doOnNext(page -> progressService.sendPageUpdate(taskId, 60, "Grammar rules explained.", chapter.getId(), dtoMapper.toDto(page)))
                    .map(page -> new GenerationContext(context.vocabulary(), dtoMapper.toDto(page.getLesson())));
        }
        else {
            return Mono.fromRunnable(() -> progressService.sendUpdate(taskId, 50, "Explaining conjugation rules..."))
                    .subscribeOn(Schedulers.boundedElastic())
                    .then(conjugationLessonService.generateLesson(request, context.vocabulary()))
                    .flatMap(lesson -> pageService.createAndPersistPage(chapter, lesson, pageCounter.getAndIncrement()))
                    .doOnNext(page -> progressService.sendPageUpdate(taskId, 60, "Conjugation rules explained.", chapter.getId(), dtoMapper.toDto(page)))
                    .map(page -> new GenerationContext(context.vocabulary(), dtoMapper.toDto(page.getLesson())));
        }
    }

    private Mono<GenerationContext> generatePracticeLessonPage(ChapterGenerationRequest request, String taskId, Chapter chapter, AtomicInteger pageCounter, GenerationContext context) {
        return Mono.fromRunnable(() -> progressService.sendUpdate(taskId, 75, "Building practice exercises..."))
                .subscribeOn(Schedulers.boundedElastic())
                .then(practiceLessonService.generateLesson(request, context.vocabulary(), context.specificLesson()))
                .flatMap(lesson -> pageService.createAndPersistPage(chapter, lesson, pageCounter.getAndIncrement()))
                .doOnNext(page -> progressService.sendPageUpdate(taskId, 85, "Practice exercises built.", chapter.getId(), dtoMapper.toDto(page)))
                .thenReturn(context);
    }

    private Mono<GenerationContext> generateReadingLessonPage(ChapterGenerationRequest request, String taskId, Chapter chapter, AtomicInteger pageCounter, GenerationContext context) {
        return Mono.fromRunnable(() -> progressService.sendUpdate(taskId, 90, "Writing reading passage..."))
                .subscribeOn(Schedulers.boundedElastic())
                .then(readingComprehensionLessonService.generateLesson(request, context.vocabulary(), context.specificLesson()))
                .flatMap(lesson -> pageService.createAndPersistPage(chapter, lesson, pageCounter.getAndIncrement()))
                .doOnNext(page -> progressService.sendPageUpdate(taskId, 100, "Chapter generation complete.", chapter.getId(), dtoMapper.toDto(page)))
                .thenReturn(context);
    }

}
